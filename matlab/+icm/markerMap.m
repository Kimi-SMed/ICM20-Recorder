function m = markerMap()
    % ECG marker-id -> label lookup (ICM_STREAM_ECG_MARKER reference table).
    % Keys are the high-byte marker codes (e.g. 0x3100). Cached after first call.

    persistent cached
    if ~isempty(cached)
        m = cached;
        return;
    end

    keys = { ...
        hex2dec('0000'), hex2dec('0100'), hex2dec('0200'), hex2dec('0300'), ...
        hex2dec('1100'), hex2dec('1200'), hex2dec('1300'), hex2dec('1400'), hex2dec('1500'), ...
        hex2dec('2100'), hex2dec('2200'), hex2dec('2300'), hex2dec('2400'), hex2dec('2500'), hex2dec('2600'), ...
        hex2dec('3100'), hex2dec('3200'), hex2dec('3300'), hex2dec('3400'), hex2dec('3500'), hex2dec('3600'), hex2dec('3700'), ...
        hex2dec('4100'), hex2dec('4200'), hex2dec('4300'), hex2dec('4400'), hex2dec('4500'), hex2dec('4600'), hex2dec('4700'), ...
        hex2dec('5100'), hex2dec('5200'), hex2dec('5300'), hex2dec('5400'), hex2dec('5500'), hex2dec('5600'), ...
        hex2dec('6100'), hex2dec('6200'), hex2dec('6300'), ...
        hex2dec('7100'), hex2dec('7200'), ...
        hex2dec('8100'), hex2dec('8200') };

    vals = { ...
        'None', 'S', 'T', 'AS', ...
        'P-ON', 'P+', 'D-P', 'P-NS', 'P-', ...
        'B-ON', 'B+', 'D-B', 'B-NS', 'B-', 'B-ReON', ...
        'VT-ON', 'VT+', 'D-VT', 'VT-NS', 'VT-', 'VT-ReON', 'VTx', ...
        'AT-ON', 'AT+', 'D-AT', 'AT-NS', 'AT-', 'AT-ReON', 'ATx', ...
        'AF-ON', 'AF+', 'D-AF', 'AF-NS', 'AF-', 'AF-ReON', ...
        'V-P', 'D-V', 'V-J', ...
        'MGT+', 'MGT-', ...
        'MP+', 'MP-' };

    cached = containers.Map(keys, vals, 'UniformValues', false);
    m = cached;
end
